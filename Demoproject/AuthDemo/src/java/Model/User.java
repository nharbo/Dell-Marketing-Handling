package Model;

/**
 *
 * @author tha
 */
public class User {
    String id;
    String name;
    String pwd;
    String gender;

    public User(String id, String name, String pwd, String gender) {
        this.id = id;
        this.name = name;
        this.pwd = pwd;
        this.gender = gender;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPwd() {
        return pwd;
    }

    public void setPwd(String pwd) {
        this.pwd = pwd;
    }
    
}
